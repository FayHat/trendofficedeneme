function updateScrollEffects() {
  scrollTicking = false;
  const viewport = window.innerHeight;
  const doc = document.documentElement;
  const scrollTop = window.scrollY || doc.scrollTop;
  const maxScroll = Math.max(1, doc.scrollHeight - viewport);

  if (scrollProgressBar) {
    scrollProgressBar.style.width = `${clamp((scrollTop / maxScroll) * 100, 0, 100)}%`;
  }

  if (topbar) {
    topbar.style.boxShadow = scrollTop > 40
      ? "0 18px 46px rgba(11,18,32,.14)"
      : "0 16px 42px rgba(11,18,32,.08)";
  }

  if (processSection && processStage && processProgress) {
    const STAGE_STICKY_TOP = 96; // must match .process-stage's CSS `top` value
    const sectionTop = processSection.offsetTop;
    const sectionHeight = processSection.offsetHeight;
    const stageHeight = processStage.offsetHeight;
    // the box only starts being pinned once scroll passes this point —
    // progress must stay at 0 before this, so frames don't shuffle while
    // the box is still scrolling into place
    const pinStart = sectionTop - STAGE_STICKY_TOP;
    const activeDistance = Math.max(1, sectionHeight - stageHeight);
    const scrolledInto = scrollTop - pinStart;
    const progress = clamp(scrolledInto / activeDistance, 0, 1);
    processProgress.style.width = `${18 + progress * 82}%`;
    const activeStep = clamp(Math.floor(progress * STAGE_STEP_COUNT), 0, STAGE_STEP_COUNT - 1);
    if (activeStep !== lastActiveStep) {
      lastActiveStep = activeStep;
      smFrames.forEach((frame, index) => {
        frame.classList.toggle("active", index === activeStep);
        frame.classList.toggle("risen", index <= activeStep);
      });
      if (smCounter) smCounter.textContent = String(activeStep + 1).padStart(2, "0");
      if (stageStepNum) stageStepNum.textContent = String(activeStep + 1).padStart(2, "0");
      // titles live in stageMetaSpans[0..3], descriptions in [4..7] — kept as
      // plain DOM nodes so the i18n switcher can update them like any other element
      if (stageStepTitle && stageMetaSpans[activeStep]) {
        stageStepTitle.textContent = stageMetaSpans[activeStep].textContent;
      }
      if (stageStepDesc && stageMetaSpans[activeStep + STAGE_STEP_COUNT]) {
        stageStepDesc.textContent = stageMetaSpans[activeStep + STAGE_STEP_COUNT].textContent;
      }
      if (smFlash) {
        smFlash.classList.remove("flashing");
        // restart the animation reliably even if triggered again quickly
        void smFlash.offsetWidth;
        smFlash.classList.add("flashing");
      }
    }
  }

  // periodically pulse the live sourcing desk nodes to feel "alive"
  if (heroInView && flowNodes.length) {
    const cycle = Math.floor(Date.now() / 1900) % flowNodes.length;
    if (cycle !== lastFlowPulse) {
      lastFlowPulse = cycle;
      flowNodes.forEach((node, index) => node.classList.toggle("pulse", index === cycle));
    }
  }

  // gentle parallax — only on ribbon figures the observer says are near the viewport
  if (ribbonActive.size) {
    ribbonActive.forEach((fig) => {
      const rect = fig.getBoundingClientRect();
      const centerOffset = (rect.top + rect.height / 2 - viewport / 2) / viewport;
      fig.style.transform = `translate3d(0, ${centerOffset * -22}px, 0)`;
    });
  }
}

