/* ---------------- custom cursor: walking figure + speed trail ---------------- */

const cursorWalker = document.getElementById("cursorWalker");
const stitchLayer = document.getElementById("cursorStitchLayer");
const hoverTargets = "a, button, .tilt-card, .quiz-options button, .map-legend button, input, select, textarea";

if (cursorWalker && stitchLayer && window.matchMedia("(hover: hover) and (pointer: fine)").matches) {
  document.body.classList.add("custom-cursor-on");
  let mouseX = -100;
  let mouseY = -100;
  let prevX = -100;
  let prevY = -100;
  let facingRight = true; // artwork's natural pose walks to the right
  let tiltDeg = 0;
  let cursorActive = false;

  // reuse a small pool of dash elements instead of creating new DOM nodes per move
  const STITCH_POOL_SIZE = 28;
  const stitchPool = [];
  for (let i = 0; i < STITCH_POOL_SIZE; i++) {
    const mark = document.createElement("div");
    mark.className = "stitch-mark";
    stitchLayer.appendChild(mark);
    stitchPool.push({ el: mark });
  }
  let stitchCursor = 0;
  let distanceSinceLastStitch = 0;
  const STITCH_SPACING = 10; // px of travel between dropped trail marks

  function dropTrailMark(x, y, angleDeg) {
    const slot = stitchPool[stitchCursor];
    stitchCursor = (stitchCursor + 1) % STITCH_POOL_SIZE;
    const el = slot.el;
    // disable transition so position snap is instant
    el.style.transition = "none";
    el.style.opacity = "1";
    el.style.transform = `translate3d(${x}px, ${y}px, 0) rotate(${angleDeg}deg) scale(1)`;
    // force layout flush — guarantees the browser registers the above values
    // before we re-enable the transition, so the fade-out reliably fires
    void el.offsetWidth;
    el.style.transition = "opacity .55s ease, transform .55s ease";
    el.style.opacity = "0";
    el.style.transform = `translate3d(${x}px, ${y}px, 0) rotate(${angleDeg}deg) scale(.4)`;
  }

  function applyWalkerTransform(flip) {
    cursorWalker.style.transform =
      `translate3d(${mouseX}px, ${mouseY}px, 0) scaleX(${flip}) rotate(${tiltDeg}deg)`;
  }

  window.addEventListener("pointermove", (event) => {
    prevX = mouseX;
    prevY = mouseY;
    mouseX = event.clientX;
    mouseY = event.clientY;

    if (!cursorActive) {
      cursorActive = true;
      cursorWalker.classList.add("active");
      prevX = mouseX;
      prevY = mouseY;
    }

    const dx = mouseX - prevX;
    const dy = mouseY - prevY;
    const moveDist = Math.hypot(dx, dy);

    // only re-aim when there's enough motion, so it doesn't jitter on tiny mouse noise
    if (moveDist > 1.2) {
      // artwork head faces LEFT by default — flip to -1 when moving right
      // so the figure always faces its direction of travel
      facingRight = dx >= 0;
      const verticalLean = Math.max(-18, Math.min(18, dy * 0.6));
      tiltDeg = facingRight ? verticalLean : -verticalLean;
    }

    applyWalkerTransform(facingRight ? -1 : 1);

    distanceSinceLastStitch += moveDist;
    if (distanceSinceLastStitch >= STITCH_SPACING) {
      distanceSinceLastStitch = 0;
      const trailAngle = Math.atan2(dy, dx) * (180 / Math.PI);
      dropTrailMark(mouseX, mouseY, trailAngle);
    }
  });

  document.addEventListener("mouseleave", () => {
    cursorActive = false;
    cursorWalker.classList.remove("active");
  });

  document.addEventListener("mouseover", (event) => {
    if (event.target.closest(hoverTargets)) {
      cursorWalker.classList.add("hovering");
    }
  });
  document.addEventListener("mouseout", (event) => {
    if (event.target.closest(hoverTargets)) {
      cursorWalker.classList.remove("hovering");
    }
  });
}

